import 'package:meno_design_system/src/core/insets/insets.dart';

/// {@template meno_spacing}
///  Class contains all space (does not matter is it vertical
///  or horizontal used in app
/// {@endtemplate}
class MenoSpacing {
  MenoSpacing._();

  /// Micro spacing of 4.0
  static const xxs = Insets.xxs;

  /// Small spacing of 8.0
  static const sm = Insets.sm;

  /// Medium spacing of 12.0
  static const md = Insets.md;

  /// Large spacing of 16.0
  static const lg = Insets.lg;

  /// Extra Large spacing of 20.0
  static const xlg = Insets.xlg;

  /// Extra extra Large spacing of 32.0
  static const xxlg = Insets.xxlg;

  /// Round Circle
  static const circle = Insets.circle;
}
